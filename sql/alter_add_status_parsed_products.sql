-- ========================================
-- Добавление колонки status в parsed_products
-- ========================================
-- Колонка хранит этап обработки записи:
-- NULL  — запись ещё не обработана
-- 1    — ссылки успешно спарсены
-- 2    — ошибка при парсинге
-- ========================================

ALTER TABLE `parsed_products`
    ADD COLUMN `status` TINYINT UNSIGNED DEFAULT NULL
    COMMENT 'Этап обработки: NULL — не обработана, 1 — ссылки спарсены, 2 — ошибка парсинга';
