-- ========================================
-- Добавление колонки price_status в parsed_products
-- ========================================
-- Позволяет различать причины отсутствия цены:
--   NULL  = ещё не обработано
--   1     = цена найдена (price содержит число)
--   2     = цена по запросу (REQUEST)
--   3     = товар найден, цена отсутствует (NO_PRICE)
--   4     = товар не найден ни на одной ссылке
-- ========================================

ALTER TABLE `parsed_products`
    ADD COLUMN `price_status` TINYINT UNSIGNED DEFAULT NULL
    COMMENT 'Статус цены: NULL=не обработано, 1=найдена, 2=по запросу, 3=нет цены, 4=не найден'
    AFTER `price`;
